<h4>Explanation of Field Types and Settings:</h4>

<dl>

<dt>Text:</dt>
<dd>A single line of text. Limited to 200 characters.

	<dl>
		<dt>Length:</dt>
		<dd>The displayed length of the field. This does not limit the amount of text allowed, just the amount shown.</dd>
	</dl>
</dd>

<dt>Textbox:</dt>
<dd>Multiple lines of text. The number of characters allowed is virtually unlimited.
	<dl>
		<dt>Height:</dt>
		<dd>How tall the textbox will be.</dd>
		<dt>Width:</dt>
		<dd>How wide the textbox will be.</dd>
	</dl>
</dd>

<dt>Dropdown:</dt>
<dd>A list of options from which to select.
	<dl>
		<dt>Options:</dt>
		<dd>You must specify the options that will appear in the list. Seperate each option with a comma (,).</dd>
	</dl>
</dd>

<dt>Yes/No:</dt>
<dd>A question for which the answer is either yes or no. There are no settings for this field type.<dl></dl></dd>

<dt>Required:</dt>
<dd>If a field is marked as required, then that field must be filled in before the registration form can be submitted.<dl></dl></dd>

</dl>