<h4>Using Reports:</h4>

<dl>

<dt>View:</dt>
<dd>To sort a column, click on the field label at the top of each column to sort by that column. Click again to reverse the order.</dd>

<dt>Download:</dt>
<dd>The report will be downloaded as an Excel-compatible spreatsheet.</dd>

</dl>

<p class="notice">
<b>Notice:</b>
<br/>
Only forms that have been Finalized will have reports available.
Reports remain available whether a form is Published or not.
<br/><br/>
If a form is Unfinalized, its report will be deleted.
</p>